    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            int index = tblProdutos.getSelectedRow();
            if(index == -1){
                JOptionPane.showMessageDialog(null, "Selecione o produto");
            }else{
                if(JOptionPane.showConfirmDialog(null, "Deseja ecluir este produto?") == 0){
                    Produto p = new Produto();
                    p.getProduto().remove(index);
                    JOptionPane.showMessageDialog(null, "Produto removido com sucesso!");
                }
            }
        }catch(Exception e){
          
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    String[] opcoes = {"Nome", "Preço"};
    int escolha = JOptionPane.showOptionDialog(
        null,                // Componente pai
        "O que deseja editar?",  // Mensagem
        "Escolha uma opção",     // Título
        JOptionPane.DEFAULT_OPTION,   // Tipo de opção
        JOptionPane.INFORMATION_MESSAGE, // Tipo de mensagem
        null,                // Ícone
        opcoes,              // Opções personalizadas
        opcoes[0]            // Valor padrão selecionado
            
    );
    if(escolha == 0){
        try{
            int i = tblProdutos.getSelectedRow();
            if(i != -1){
                String nome = JOptionPane.showInputDialog("Digite o nome produto");
                if(!nome.isEmpty()){
                    int id = Integer.parseInt(String.valueOf(tblProdutos.getValueAt(i, 0)));
                    ProdutoDao produtosDao = new ProdutoDao();
                    produtosDao.editarNomeProduto(nome, id);
                    preencherTabela();
                }else{
                    JOptionPane.showMessageDialog(null, "Nada foi digitado");
                    }
            }else{
                JOptionPane.showMessageDialog(null, "Selecione um produto");
            }
            
        }catch(Exception e){
            
        }
       
    }
    else if(escolha == 1){
        try{
            int i = tblProdutos.getSelectedRow();
            if(i != -1){ 
                int id = Integer.parseInt(String.valueOf(tblProdutos.getValueAt(i, 0)));
                double preco = Double.parseDouble(JOptionPane.showInputDialog("Digite o preço do produto"));
                 ProdutoDao produtosDao = new ProdutoDao();
                 produtosDao.editarPrecoProduto(preco, id);
                 preencherTabela();
             }else{
                JOptionPane.showMessageDialog(null, "Selecione um produto");
            }
            
        }catch(Exception e){

        }
        
    }
    
        
    }//GEN-LAST:event_jButton2ActionPerformed

