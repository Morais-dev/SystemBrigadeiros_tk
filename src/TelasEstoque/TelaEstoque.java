    private void preencherTabela(){
        EstoqueDao estoque = new EstoqueDao();
        ArrayList<Estoque> lista = estoque.getEstoque(txtNome.getText());
        tblEstoque.getColumnModel().getColumn(0).setPreferredWidth(10);
        tblEstoque.getColumnModel().getColumn(3).setPreferredWidth(10);
        
        DefaultTableModel tabela = (DefaultTableModel) tblEstoque.getModel();
        tblEstoque.setRowSorter(new TableRowSorter(tabela));
        tabela.setRowCount(0); 
        
        
        for (Estoque e : lista) {
            Object[] obj = new Object[] {
                e.getId(),
                e.getNome(),
                e.getSabor(),
                e.getQtd()
            };
            tabela.addRow(obj);
        }
        
    }

