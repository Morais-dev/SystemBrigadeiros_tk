    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            int i = Integer.parseInt(String.valueOf(tblEstoque.getSelectedRow()));
            if(i == -1){
                JOptionPane.showMessageDialog(rootPane, "Selecione um estoque para editar");
                return;
            }else{
                int id = Integer.parseInt(String.valueOf(tblEstoque.getValueAt(i, 0)));
                int qtd = Integer.parseInt(JOptionPane.showInputDialog(null, "Qual a quantidade de " + "em estoque?"));
                EstoqueDao estoqueDao = new EstoqueDao();
                estoqueDao.editar(qtd, id);
                preencherTabela();
            }
        }catch(Exception e){
            
        }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

